def get_text(language: str, key: str) -> str:
    lang = language if language in TEXTS else "fr"
    if key in TEXTS[lang]:
        return TEXTS[lang][key]
    return TEXTS["fr"].get(key, key)


def format_text(language: str, key: str, **kwargs) -> str:
    template = get_text(language, key)
    try:
        return template.format(**kwargs)
    except Exception:
        return template
